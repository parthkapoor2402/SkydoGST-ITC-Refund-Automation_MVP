const GST_PORTAL_URL = 'https://www.gst.gov.in/'

function safePart(s: string, max = 48): string {
  return s
    .trim()
    .replace(/[^\w.-]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_|_$/g, '')
    .slice(0, max) || 'NA'
}

function periodSlug(summary: SummaryReport): string {
  const a =
    safePart(summary.periodFrom.replace(/\//g, ''), 12) || 'NA'
  const b = safePart(summary.periodTo.replace(/\//g, ''), 12) || 'NA'
  return `${a}_to_${b}`
}

function bundleDateStamp(): string {
  return new Date().toISOString().slice(0, 10).replace(/-/g, '')
}

export function getBundleZipBaseName(
  gstin: string,
  summary: SummaryReport,
): string {
  const g = safePart(gstin, 20) || 'UNKNOWN'
  return `GST_Refund_Bundle_${g}_${periodSlug(summary)}_${bundleDateStamp()}`
}

function csvEscape(value: string): string {
  if (/[",\r\n]/.test(value)) {
    return `"${value.replace(/"/g, '""')}"`
  }
  return value
}

